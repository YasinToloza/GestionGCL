package com.biblioteca.entidad;

public class Objeto {
	private int CodigoObj;
	private String NomObj, DescObj;
	
	
	public int getCodigoObj() {
		return CodigoObj;
	}
	public void setCodigoObj(int codigoObj) {
		CodigoObj = codigoObj;
	}
	public String getNomObj() {
		return NomObj;
	}
	public void setNomObj(String nomObj) {
		NomObj = nomObj;
	}
	public String getDescObj() {
		return DescObj;
	}
	public void setDescObj(String descObj) {
		DescObj = descObj;
	}
	
	

	
}
