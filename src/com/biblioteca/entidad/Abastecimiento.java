package com.biblioteca.entidad;

public class Abastecimiento {
	private int codigoSol, codigoEst, codigoTipSol,codemp; 
	private String descripSoli, fecha;
	
	
	private String nombreTipo,nombreSolicitud;
	

	
	
	public int getCodemp() {
		return codemp;
	}

	public void setCodemp(int codemp) {
		this.codemp = codemp;
	}

	public String getNombreSolicitud() {
		return nombreSolicitud;
	}

	public void setNombreSolicitud(String nombreSolicitud) {
		this.nombreSolicitud = nombreSolicitud;
	}

	public int getCodigoSol() {
		return codigoSol;
	}

	public void setCodigoSol(int codigoSol) {
		this.codigoSol = codigoSol;
	}

	public int getCodigoEst() {
		return codigoEst;
	}

	public void setCodigoEst(int codigoEst) {
		this.codigoEst = codigoEst;
	}

	public int getCodigoTipSol() {
		return codigoTipSol;
	}

	public void setCodigoTipSol(int codigoTipSol) {
		this.codigoTipSol = codigoTipSol;
	}

	public String getDescripSoli() {
		return descripSoli;
	}

	public void setDescripSoli(String descripSoli) {
		this.descripSoli = descripSoli;
	}

	public String getFecha() {
		return fecha;
	}

	public void setFecha(String fecha) {
		this.fecha = fecha;
	}

	public String getNombreTipo() {
		return nombreTipo;
	}

	public void setNombreTipo(String nombreTipo) {
		this.nombreTipo = nombreTipo;
	}
	
	
}
