package com.biblioteca.entidad;

public class CCP {
	private int codigoccp,empleado,estado;
	private double montoccp;
	private String nombreEstado,desccp,fecha;
	
	public int getCodigoccp() {
		return codigoccp;
	}
	public void setCodigoccp(int codigoccp) {
		this.codigoccp = codigoccp;
	}
	public int getEmpleado() {
		return empleado;
	}
	public void setEmpleado(int empleado) {
		this.empleado = empleado;
	}
	public int getEstado() {
		return estado;
	}
	public void setEstado(int estado) {
		this.estado = estado;
	}
	public double getMontoccp() {
		return montoccp;
	}
	public void setMontoccp(double montoccp) {
		this.montoccp = montoccp;
	}
	public String getNombreEstado() {
		return nombreEstado;
	}
	public void setNombreEstado(String nombreEstado) {
		this.nombreEstado = nombreEstado;
	}
	public String getDesccp() {
		return desccp;
	}
	public void setDesccp(String desccp) {
		this.desccp = desccp;
	}
	public String getFecha() {
		return fecha;
	}
	public void setFecha(String fecha) {
		this.fecha = fecha;
	}
	
	
	
}
