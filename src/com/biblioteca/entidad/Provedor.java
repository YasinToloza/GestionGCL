package com.biblioteca.entidad;

public class Provedor {

	private int codPro,estado , celular, codObjeto;
	private double precio;
	private String Direc ,ruc,razon;
	private String monEstado;
	
	public int getCodPro() {
		return codPro;
	}
	public void setCodPro(int codPro) {
		this.codPro = codPro;
	}
	public int getEstado() {
		return estado;
	}
	public void setEstado(int estado) {
		this.estado = estado;
	}
	public int getCelular() {
		return celular;
	}
	public void setCelular(int celular) {
		this.celular = celular;
	}
	public int getCodObjeto() {
		return codObjeto;
	}
	public void setCodObjeto(int codObjeto) {
		this.codObjeto = codObjeto;
	}
	public double getPrecio() {
		return precio;
	}
	public void setPrecio(double precio) {
		this.precio = precio;
	}
	public String getDirec() {
		return Direc;
	}
	public void setDirec(String direc) {
		Direc = direc;
	}
	public String getRuc() {
		return ruc;
	}
	public void setRuc(String ruc) {
		this.ruc = ruc;
	}
	public String getRazon() {
		return razon;
	}
	public void setRazon(String razon) {
		this.razon = razon;
	}
	public String getMonEstado() {
		return monEstado;
	}
	public void setMonEstado(String monEstado) {
		this.monEstado = monEstado;
	}
	
	
	
}
